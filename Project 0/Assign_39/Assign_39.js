function city_country(cityName, ctryName) {
    return ("".concat(cityName, ", ").concat(ctryName));
    // console.log(`${cityName} is city of ${ctryName}.`)
}
console.log(city_country("Karachi", "Pakistan"));
console.log(city_country("Madina", "Saudi Arabia"));
console.log(city_country("London", "England"));
